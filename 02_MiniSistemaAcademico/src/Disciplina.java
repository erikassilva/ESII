public class Disciplina {
	
	private String siglaDisciplina;
	private String nomeDisciplina;
	
	public Disciplina(String siglaDisciplina, String nomeDisciplina) {
		this.siglaDisciplina = siglaDisciplina;
		this.nomeDisciplina = nomeDisciplina;
	}
			
	public String getSiglaDisciplina() { return siglaDisciplina; }
	public void setSiglaDisciplina(String siglaDisciplina) { this.siglaDisciplina = siglaDisciplina; }
	public String getNomeDisciplina() { return nomeDisciplina; }
	public void setNomeDisciplina(String nomeDisciplina) { this.nomeDisciplina = nomeDisciplina; }
	
	

}
	
